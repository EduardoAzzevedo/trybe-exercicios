const a = 5;
const b = 7;

//Adição
if (a == 5 || b == 7) {
    console.log(a + b);
}
//Subtração
if (a == 5 || b == 7) {
    console.log(a - b);
}
//Multiplicação
if (a == 5 || b == 7) {
    console.log(a * b);
}
//Divisão
if (a <= 5 && b >= 7) {
    console.log(a / b);
}
//Módulo
if (a === 5 && b === 7) {
    console.log(a % b);
}